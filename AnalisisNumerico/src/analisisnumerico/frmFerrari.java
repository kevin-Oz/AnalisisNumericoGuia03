    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed

        double a, b, c, d,cuarta;

 
        String f=txtFuncci.getText();
        
        double[] coeficientes= Funcion.SacarCoeficientes(f);
        a=coeficientes[1];
        b=coeficientes[2];
        c=coeficientes[3];
        d=coeficientes[4];
       cuarta=coeficientes[0];

     if (cuarta != 1) {
            a = a / cuarta;
            b = b / cuarta;
            c = c / cuarta;
            d=d/cuarta;
            cuarta = 1;
        }

        double raiz[] = ProcesoTarFerrari.ferrari(a, b, c, d);

        txtRaiz1.setText(String.valueOf(raiz[0]));
        txtRaiz2.setText(String.valueOf(raiz[1]));
        txtRaiz3.setText(String.valueOf(raiz[2]));
        txtRaiz4.setText(String.valueOf(raiz[3]));
    }//GEN-LAST:event_btnCalcularActionPerformed

